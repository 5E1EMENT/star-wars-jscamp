/**
 * Interface for film data structure
 */

export interface Film {
    /**
     * title - film name
     */
    title: string;
    /**
     * releaseDate - release date
     */
    releaseDate: Date;
    /**
     * episodeId - episode number
     */
    episodeId: number;
    /**
     * director - episode director
     */
    director: string;
}
