/**
 * Detailed film class
 */
export class DetailFilm {
  /**
   * Field title - film name
   */
  title: string;
  /**
   * Field releaseDate - release date
   */
  releaseDate: Date;
  /**
   * Field episodeId - episode number
   */
  episodeId: number;
  /**
   * Field director - episode director
   */
  director: string;
  /**
   * Opening crawl
   */
  openingCrawl: string;
  /**
   * Producer
   */
  producer: string;
  /**
   * Characters
   */
  characters: number[];
  /**
   * Planets
   */
  planets: number[];
  /**
   * Species
   */
  species: number[];
  /**
   * Starships
   */
  starships: number[];
  /**
   * vehicles
   */
  vehicles: number[];
}
